     */
    public function store(Request $request)
    {
        $input = $request->all();

        if($file = $request->file('image')){

            $name = time().$file->getClientOriginalName();
            $file->move('images',$name);
            $input['image'] = $name;
        }
         Strongpoint::create($input);
         return "success";
         
